"""
Тест добавления фотографий в бота
"""
import os

def test_photos():
    print("=" * 60)
    print("🖼️  ТЕСТИРОВАНИЕ ДОБАВЛЕНИЯ ФОТОГРАФИЙ")
    print("=" * 60)
    
    # Список всех фотографий, которые должны быть
    required_photos = {
        "555.jpg": "Главное меню Тату",
        "123.jpg": "Мифы о татуировках (1)",
        "345.jpg": "Мифы о татуировках (2)",
        "666.jpg": "Коррекции (1)",
        "777.jpg": "Коррекции (2)",
        "888.jpg": "Больно ли делать тату (1)",
        "999.jpg": "Больно ли делать тату (2)",
        "155.jpg": "Стоимость татуировки (1)",
        "255.jpg": "Стоимость татуировки (2)"
    }
    
    print("\n📋 ПРОВЕРКА НАЛИЧИЯ ФОТОГРАФИЙ:")
    print("-" * 60)
    
    found = 0
    missing = 0
    
    for photo, description in required_photos.items():
        if os.path.exists(photo):
            print(f"✅ {photo} - {description}")
            found += 1
        else:
            print(f"⚠️  {photo} - {description} (НЕ НАЙДЕНА)")
            missing += 1
    
    print("\n" + "=" * 60)
    print("📊 СТАТИСТИКА:")
    print("=" * 60)
    print(f"✅ Найдено фотографий: {found}")
    print(f"⚠️  Отсутствует фотографий: {missing}")
    
    if missing > 0:
        print("\n⚠️  ВНИМАНИЕ:")
        print("Некоторые фотографии не найдены в папке проекта.")
        print("Пожалуйста, добавьте их для корректной работы бота.")
        print("\nБот будет работать, но фотографии не будут отображаться")
        print("до тех пор, пока вы не добавите файлы в папку проекта.")
    else:
        print("\n🎉 ВСЕ ФОТОГРАФИИ НА МЕСТЕ!")
    
    print("\n" + "=" * 60)
    print("📝 ГДЕ ИСПОЛЬЗУЮТСЯ ФОТОГРАФИИ:")
    print("=" * 60)
    print("1. 555.jpg - Главное меню раздела 'Тату'")
    print("2. 123.jpg, 345.jpg - Раздел 'Мифы о татуировках'")
    print("3. 666.jpg, 777.jpg - Раздел 'Коррекции'")
    print("4. 888.jpg, 999.jpg - Раздел 'Больно ли делать тату'")
    print("5. 155.jpg, 255.jpg - Раздел 'Стоимость татуировки'")
    
    print("\n" + "=" * 60)
    print("✅ КОД ДЛЯ ОТПРАВКИ ФОТОГРАФИЙ ДОБАВЛЕН В БОТ")
    print("=" * 60)
    
    return found, missing

if __name__ == "__main__":
    found, missing = test_photos()
    
    if missing == 0:
        print("\n🚀 БОТ ПОЛНОСТЬЮ ГОТОВ К РАБОТЕ!")
    else:
        print(f"\n⚠️  Добавьте {missing} фотографий для полной функциональности")
